    private void jButtonAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAbrirActionPerformed
        String Nombre1,Nombre2,Apellido1,Apellido2,NoID,Especializacion,A単oEsperiencia;
        String au = "RegistroDetective.Dt";
        Scanner linea = null;
        File doc_le = new File(au);
        try {
            linea = new Scanner(doc_le);
            while (linea.hasNextLine()) {                
                Nombre1 = linea.nextLine();
                Nombre2 = linea.nextLine();
                Apellido1 = linea.nextLine();
                Apellido2 = linea.nextLine();
                NoID = linea.nextLine();
                Especializacion = linea.nextLine();
                A単oEsperiencia = linea.nextLine();
                model.addRow(new Object[]{Nombre1,Nombre2,Apellido1,Apellido2,NoID,Especializacion,A単oEsperiencia});
                
            }
        } catch (Exception x) {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_jButtonAbrirActionPerformed

